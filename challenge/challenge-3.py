primeiro_valor = int(input('Digite um valor: '))
segundo_valor = int(input('Digite outro valor: '))
if primeiro_valor > segundo_valor:
    print(f'o primeiro {primeiro_valor} é maior que o segundo valor {segundo_valor}')
elif primeiro_valor == segundo_valor:
    print(f'o valores são iguais!')
else:
    print(f'o segundo valor {segundo_valor} é maior que o {primeiro_valor} valor')